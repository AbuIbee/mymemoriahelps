// Helper function to get today's routines
export const getTodaysRoutines = (): Routine[] => {
  const today = new Date().toLocaleDateString('en-US', { weekday: 'long' }).toLowerCase();
  return mockRoutines.filter(routine => 
    routine.days.includes(today as any)
  );
};

// Helper function to get today's medications
export const getTodaysMedications = (): Medication[] => {
  return mockMedications.filter(med => med.remindersEnabled);
};

// Helper function to get upcoming appointments
export const getUpcomingAppointments = (): Appointment[] => {
  return mockAppointments
    .filter(apt => !apt.completed && apt.dateTime > new Date())
    .sort((a, b) => a.dateTime.getTime() - b.dateTime.getTime());
};
